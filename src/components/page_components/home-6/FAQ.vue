        const switchElement = document.getElementById('switch');
        const atcLabel = document.querySelector('.atc');
        const abcLabel = document.querySelector('.abc');

        // Show the first tab content
        faqTabContents[0].style.display = 'block';

        // Add change event listener to the switch
        switchElement.addEventListener('change', function () {
            // Hide all tab contents
            faqTabContents.forEach((content) => content.style.display = 'none');
            atcLabel.classList.remove('cd');
            abcLabel.classList.remove('cd');

            // Show the selected tab content with fade-in animation
            if (this.checked) {
                document.getElementById('faqTwo').style.display = 'block';
                atcLabel.classList.add('cd');
            } else {
                document.getElementById('faqOne').style.display = 'block';
                abcLabel.classList.add('cd');
            }
        });
    }
